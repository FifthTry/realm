    pub fn from_accept_language_header(h: Option<String>, default: Self) -> Self {
        if let Some(v) = h {
            for code in accept_language::parse(v.as_str()).iter() {
                let code = code.split('-').next().unwrap_or(code);
                if let Ok(lang) = Self::from_2_letter_code(code) {
                    return lang;
                }
            }
        }

        default
    }
